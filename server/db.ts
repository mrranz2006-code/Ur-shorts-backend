import { eq } from "drizzle-orm";
import { drizzle } from "drizzle-orm/mysql2";
import { InsertUser, users } from "../drizzle/schema";
import { ENV } from "./_core/env";

let _db: ReturnType<typeof drizzle> | null = null;

// Lazily create the drizzle instance so local tooling can run without a DB.
export async function getDb() {
  if (!_db && process.env.DATABASE_URL) {
    try {
      _db = drizzle(process.env.DATABASE_URL);
    } catch (error) {
      console.warn("[Database] Failed to connect:", error);
      _db = null;
    }
  }
  return _db;
}

export async function upsertUser(user: Partial<InsertUser> & { openId: string }): Promise<void> {
  if (!user.openId) {
    throw new Error("User openId is required for upsert");
  }

  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot upsert user: database not available");
    return;
  }

  try {
    const values: Partial<InsertUser> & { openId: string; lastSignedIn: Date } = {
      openId: user.openId,
      lastSignedIn: user.lastSignedIn || new Date(),
    };

    const updateSet: Record<string, unknown> = {
      lastSignedIn: values.lastSignedIn,
    };

    // Optional fields
    if (user.username) {
      values.username = user.username;
      updateSet.username = user.username;
    }
    if (user.email) {
      values.email = user.email;
      updateSet.email = user.email;
    }
    if (user.loginMethod) {
      values.loginMethod = user.loginMethod;
      updateSet.loginMethod = user.loginMethod;
    }
    if (user.phone) {
      values.phone = user.phone;
      updateSet.phone = user.phone;
    }
    if (user.profilePhoto) {
      values.profilePhoto = user.profilePhoto;
      updateSet.profilePhoto = user.profilePhoto;
    }
    if (user.bio) {
      values.bio = user.bio;
      updateSet.bio = user.bio;
    }

    // Handle role
    if (user.role !== undefined) {
      values.role = user.role;
      updateSet.role = user.role;
    } else if (user.openId === ENV.ownerOpenId) {
      values.role = "admin";
      updateSet.role = "admin";
    } else {
      values.role = "user";
      updateSet.role = "user";
    }

    await db.insert(users).values(values as InsertUser).onDuplicateKeyUpdate({
      set: updateSet,
    });
  } catch (error) {
    console.error("[Database] Failed to upsert user:", error);
    throw error;
  }
}

export async function getUserByOpenId(openId: string) {
  const db = await getDb();
  if (!db) {
    console.warn("[Database] Cannot get user: database not available");
    return undefined;
  }

  const result = await db.select().from(users).where(eq(users.openId, openId)).limit(1);

  return result.length > 0 ? result[0] : undefined;
}

